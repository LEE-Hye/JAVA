
public class Address {
	
	// 林家废 
	private String name;
	private int mail;
	private String tel;

	
	// 积己磊
	public Address(String name, int mail, String tel) {
		this.name = name;
		this.mail = mail;
		this.tel = tel;
	}
	
	
	public String getName() {
		return name;
	}
	public int getMail() {
		return mail;
	}
	public String getTel() {
		return tel;
	}

}
